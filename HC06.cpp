/* Includes ------------------------------------------------------------------*/
#include <Arduino.h>
#include "HC06.h"

/* Macro ---------------------------------------------------------------------*/
#define BTRX 11
#define BTTX 12
/* Variable ------------------------------------------------------------------*/
SoftwareSerial BTserial(BTRX,BTTX);
/* Function ------------------------------------------------------------------*/
/*
 *@description:蓝牙串口初始化
 *@input:无
 *@output:无
*/
void HC06_Init(void)
{
    BTserial.begin(9600);
}

